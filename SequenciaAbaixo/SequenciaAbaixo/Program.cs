using System;

namespace SequenciaAbaixo
{
    class Program
    {
        static void Main(string[] args)
        {
            for (int i = 1; i <= 9; i += 2)
            {
                for (int j = 7; j > 4; j--)    //escreva seu código nos espaços em branco
                {
                    Console.WriteLine($"I={i} J={j}");
                }
            }
        }
    }
}
